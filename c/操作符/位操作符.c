#include <stdio.h>
int main()
{
    int a=3;
    //00000000000000000000000000000011
    int b=-5;
    //10000000000000000000000000000101原码
    //11111111111111111111111111111011补码
    int c=a&b;
    //00000000000000000000000000000011补码(正数原码补码相同)
    int d=a|b;
    //11111111111111111111111111111011补码
    //10000000000000000000000000000101原码
    int e=a^b;
    //11111111111111111111111111111000补码
    //11111111111111111111111111110111反码
    //10000000000000000000000000001000原码
    printf("%d %d %d\n",c,d,e);
    //打印出的是原码
}